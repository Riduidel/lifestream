package org.ndx.lifestream.configuration;

public class UnableToLoadLinksCatalogException extends UnableToConfigureCacheException {

	public UnableToLoadLinksCatalogException() {
	}

	public UnableToLoadLinksCatalogException(String message) {
		super(message);
	}

	public UnableToLoadLinksCatalogException(Throwable cause) {
		super(cause);
	}

	public UnableToLoadLinksCatalogException(String message, Throwable cause) {
		super(message, cause);
	}

	public UnableToLoadLinksCatalogException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

}
