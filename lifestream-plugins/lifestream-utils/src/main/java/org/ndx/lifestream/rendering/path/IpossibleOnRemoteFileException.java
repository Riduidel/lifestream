package org.ndx.lifestream.rendering.path;

import org.ndx.lifestream.utils.exception.LifestreamException;

public class IpossibleOnRemoteFileException extends LifestreamException {

	public IpossibleOnRemoteFileException() {
	}

	public IpossibleOnRemoteFileException(String message) {
		super(message);
	}

	public IpossibleOnRemoteFileException(Throwable cause) {
		super(cause);
	}

	public IpossibleOnRemoteFileException(String message, Throwable cause) {
		super(message, cause);
	}

	public IpossibleOnRemoteFileException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

}
